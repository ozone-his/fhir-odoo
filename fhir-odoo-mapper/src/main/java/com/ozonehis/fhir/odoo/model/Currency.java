package com.ozonehis.fhir.odoo.model;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;
import com.fasterxml.jackson.annotation.JsonInclude;
import com.fasterxml.jackson.annotation.JsonProperty;
import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.EqualsAndHashCode;
import lombok.NoArgsConstructor;

@Data
@AllArgsConstructor
@NoArgsConstructor
@EqualsAndHashCode(callSuper = true)
@JsonInclude(JsonInclude.Include.NON_NULL)
@JsonIgnoreProperties(ignoreUnknown = true)
public class Currency extends BaseOdooModel {

    @JsonProperty("active")
    private boolean active;

    @JsonProperty("currency_unit_label")
    private String currencyUnitLabel;

    @JsonProperty("currency_subunit_label")
    private String currencySubunitLabel;

    @JsonProperty("decimal_places")
    private int decimalPlaces;
}
